package javaPractise;

public class Person {
	
	private String name;
	private long phone;
	private String email;
	
	
	public void setName(String name) {
		this.name = name;
	}
		
	public String getName() {
		return name;
	}

	public long getPhone() {
		return phone;
	}

	public void setPhone(int phone) {
		this.phone = phone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
	
	

	
}
